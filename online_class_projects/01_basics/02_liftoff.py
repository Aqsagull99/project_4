def main():
    # Countdown from 10 to 1
    for i in range(10, 0, -1):  # Starts from 10, decreases by 1 until 1
        print(i, end=" ")  # Print numbers in the same line

    print("Liftoff!")  # Print Liftoff at the end


# Required to call the main function
if __name__ == '__main__':
    main()
